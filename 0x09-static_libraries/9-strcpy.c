#include "main.h"
#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>
/**
 * _strcpy - copies the string that is pointed by the src
 * @dest: copies to
 * @src: from where we can copy
 * Return: string
 */
char *_strcpy(char *dest, char *src)
{
	return strcpy(dest, src);
}
